### Nessa pasta estão todos os arquivos que serão utilizados no botão Comandos ###

# Path: Comandos/__innit__.py 
    # Arquivo criado para que a pasta Comandos seja reconhecida como um pacote. Também utilizado para comentários gerais dos arquivos pertencentes a pasta

# Path: Comandos/Comandos_Screen.py
    # Nesse arquiivo é criado a tela de comandos, com uma lista de checkbox que representam os comandos mais utlizados nos inventários, uma checkbox vazia para digitação de um comando livre e um botão para distorção de saldo.

# Path: Comandos/Comandos_Func.py
    # Nesse arquivo estão todas as funções que serão utilizadas na tela de comandos, como a função que cria a lista de comandos, a função que itera sobre essa lista executando os comandos marcados, a função que verifica se o comando está marcado e a função que valida o valor digitado no Entry como float aceitando virgula na casa decimal e bloqueando o uso de ponto.

# Path: Comandos/Comandos_Dist_Saldo.py
    # Nesse arquivo está a função que cria a tela e executa o comando de distorção de saldo, diferente dos outros, aqui tela e funções pertecem ao mesmo arquivo por motivos apenas logísticos, já que não compensaria criar um arquivo para cada